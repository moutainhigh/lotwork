    <p align="left" class="MsoNormal">
        <b><span style="FONT-SIZE: 13.5pt; FONT-FAMILY: 宋体">二、双色球普通玩法有那些选号方式？</span><span 
            lang="EN-US" style="FONT-SIZE: 13.5pt; FONT-FAMILY: Arial, sans-serif">&nbsp;</span></b><span 
            lang="EN-US" style="FONT-SIZE: 13.5pt; FONT-FAMILY: Arial, sans-serif">&nbsp;</span></p>
    <p align="left" class="MsoNormal">
        <span lang="EN-US" style="FONT-SIZE: 13.5pt; FONT-FAMILY: Arial, sans-serif"><O:P></O:P>
        </span>
    </p>
    <p align="left" class="MsoNormal" 
        style="MARGIN-BOTTOM: 12pt; MARGIN-LEFT: 36pt; TEXT-INDENT: -18pt">
        &nbsp;</p>
    <p align="left" class="MsoNormal" 
        style="MARGIN-BOTTOM: 12pt; MARGIN-LEFT: 36pt; TEXT-INDENT: -18pt">
        &lt;!--[if !supportLists]--&gt;<span lang="EN-US" 
            style="FONT-SIZE: 13.5pt; FONT-FAMILY: Arial, sans-serif">1.<span 
            style="FONT-SIZE: 7pt; FONT-FAMILY: 'Times New Roman'">&nbsp;&nbsp; </span>
        </span>&lt;!--[endif]--&gt;<span style="FONT-SIZE: 13.5pt; FONT-FAMILY: 宋体">复式：</span><span 
            lang="EN-US" style="FONT-SIZE: 13.5pt; FONT-FAMILY: Arial, sans-serif"><br />
        </span><span style="FONT-SIZE: 13.5pt; FONT-FAMILY: 宋体">从</span><span 
            lang="EN-US" style="FONT-SIZE: 13.5pt; FONT-FAMILY: Arial, sans-serif">01-33</span><span 
            style="FONT-SIZE: 13.5pt; FONT-FAMILY: 宋体">共</span><span lang="EN-US" 
            style="FONT-SIZE: 13.5pt; FONT-FAMILY: Arial, sans-serif">33</span><span 
            style="FONT-SIZE: 13.5pt; FONT-FAMILY: 宋体">个红色号码球中选择</span><span 
            lang="EN-US" style="FONT-SIZE: 13.5pt; FONT-FAMILY: Arial, sans-serif">6-12</span><span 
            style="FONT-SIZE: 13.5pt; FONT-FAMILY: 宋体">个号码，并从</span><span lang="EN-US" 
            style="FONT-SIZE: 13.5pt; FONT-FAMILY: Arial, sans-serif">01-16</span><span 
            style="FONT-SIZE: 13.5pt; FONT-FAMILY: 宋体">共</span><span lang="EN-US" 
            style="FONT-SIZE: 13.5pt; FONT-FAMILY: Arial, sans-serif">16</span><span 
            style="FONT-SIZE: 13.5pt; FONT-FAMILY: 宋体">个蓝色号码球中选择</span><span 
            lang="EN-US" style="FONT-SIZE: 13.5pt; FONT-FAMILY: Arial, sans-serif">1-8</span><span 
            style="FONT-SIZE: 13.5pt; FONT-FAMILY: 宋体">个号码，</span><span lang="EN-US" 
            style="FONT-SIZE: 13.5pt; FONT-FAMILY: Arial, sans-serif"><br />
        </span><span style="FONT-SIZE: 13.5pt; FONT-FAMILY: 宋体">组合成多注投注号码的投注。</span><span 
            lang="EN-US" style="FONT-SIZE: 13.5pt; FONT-FAMILY: Arial, sans-serif"><O:P></O:P></span></p>
    <p align="left" class="MsoNormal" 
        style="MARGIN-BOTTOM: 12pt; MARGIN-LEFT: 36pt; TEXT-INDENT: -18pt">
        &lt;!--[if !supportLists]--&gt;<span lang="EN-US" 
            style="FONT-SIZE: 13.5pt; FONT-FAMILY: Arial, sans-serif">2.<span 
            style="FONT-SIZE: 7pt; FONT-FAMILY: 'Times New Roman'">&nbsp;&nbsp; </span>
        </span>&lt;!--[endif]--&gt;<span style="FONT-SIZE: 13.5pt; FONT-FAMILY: 宋体">单式：</span><span 
            lang="EN-US" style="FONT-SIZE: 13.5pt; FONT-FAMILY: Arial, sans-serif"><br />
        </span><span style="FONT-SIZE: 13.5pt; FONT-FAMILY: 宋体">在单式输入框当中，手动输入至少一组</span><span 
            lang="EN-US" style="FONT-SIZE: 13.5pt; FONT-FAMILY: Arial, sans-serif">6</span><span 
            style="FONT-SIZE: 13.5pt; FONT-FAMILY: 宋体">红球号码加上</span><span lang="EN-US" 
            style="FONT-SIZE: 13.5pt; FONT-FAMILY: Arial, sans-serif">1</span><span 
            style="FONT-SIZE: 13.5pt; FONT-FAMILY: 宋体">蓝球号码，或者将投注号码（</span><span 
            lang="EN-US" style="FONT-SIZE: 13.5pt; FONT-FAMILY: Arial, sans-serif">6</span><span 
            style="FONT-SIZE: 13.5pt; FONT-FAMILY: 宋体">红</span><span lang="EN-US" 
            style="FONT-SIZE: 13.5pt; FONT-FAMILY: Arial, sans-serif">+1</span><span 
            style="FONT-SIZE: 13.5pt; FONT-FAMILY: 宋体">蓝）</span><span lang="EN-US" 
            style="FONT-SIZE: 13.5pt; FONT-FAMILY: Arial, sans-serif"><br />
        </span><span style="FONT-SIZE: 13.5pt; FONT-FAMILY: 宋体">以文本（</span><span 
            lang="EN-US" style="FONT-SIZE: 13.5pt; FONT-FAMILY: Arial, sans-serif">.txt</span><span 
            style="FONT-SIZE: 13.5pt; FONT-FAMILY: 宋体">）格式导入投注方案。单式的标准格式：</span><span 
            lang="EN-US" style="FONT-SIZE: 13.5pt; FONT-FAMILY: Arial, sans-serif">“01,02,03,04,05,06+01”</span><span 
            style="FONT-SIZE: 13.5pt; FONT-FAMILY: 宋体">。</span><span lang="EN-US" 
            style="FONT-SIZE: 13.5pt; FONT-FAMILY: Arial, sans-serif"><O:P></O:P></span></p>
    <p align="left" class="MsoNormal" 
        style="MARGIN-BOTTOM: 12pt; MARGIN-LEFT: 36pt; TEXT-INDENT: -18pt">
        &lt;!--[if !supportLists]--&gt;<span lang="EN-US" 
            style="FONT-SIZE: 13.5pt; FONT-FAMILY: Arial, sans-serif">3.<span 
            style="FONT-SIZE: 7pt; FONT-FAMILY: 'Times New Roman'">&nbsp;&nbsp; </span>
        </span>&lt;!--[endif]--&gt;<span style="FONT-SIZE: 13.5pt; FONT-FAMILY: 宋体">胆拖：</span><span 
            lang="EN-US" style="FONT-SIZE: 13.5pt; FONT-FAMILY: Arial, sans-serif"><br />
        </span><span style="FONT-SIZE: 13.5pt; FONT-FAMILY: 宋体">
        双色球胆拖为一种方便用户选号的方式，分别从红球胆码区及拖码区当中选取胆码和拖码，其</span><span lang="EN-US" 
            style="FONT-SIZE: 13.5pt; FONT-FAMILY: Arial, sans-serif"><br />
        </span><span style="FONT-SIZE: 13.5pt; FONT-FAMILY: 宋体">中胆码选择</span><span 
            lang="EN-US" style="FONT-SIZE: 13.5pt; FONT-FAMILY: Arial, sans-serif">1-5</span><span 
            style="FONT-SIZE: 13.5pt; FONT-FAMILY: 宋体">个，胆码加拖码个数</span><span 
            lang="EN-US" style="FONT-SIZE: 13.5pt; FONT-FAMILY: Arial, sans-serif">6~12</span><span 
            style="FONT-SIZE: 13.5pt; FONT-FAMILY: 宋体">个，同时在蓝球区选取</span><span 
            lang="EN-US" style="FONT-SIZE: 13.5pt; FONT-FAMILY: Arial, sans-serif">1</span><span 
            style="FONT-SIZE: 13.5pt; FONT-FAMILY: 宋体">至</span><span lang="EN-US" 
            style="FONT-SIZE: 13.5pt; FONT-FAMILY: Arial, sans-serif">8</span><span 
            style="FONT-SIZE: 13.5pt; FONT-FAMILY: 宋体">个号码，组成至少</span><span 
            lang="EN-US" style="FONT-SIZE: 13.5pt; FONT-FAMILY: Arial, sans-serif">1</span><span 
            style="FONT-SIZE: 13.5pt; FONT-FAMILY: 宋体">注</span><span lang="EN-US" 
            style="FONT-SIZE: 13.5pt; FONT-FAMILY: Arial, sans-serif">6</span><span 
            style="FONT-SIZE: 13.5pt; FONT-FAMILY: 宋体">红</span><span lang="EN-US" 
            style="FONT-SIZE: 13.5pt; FONT-FAMILY: Arial, sans-serif"><br />
        </span><span style="FONT-SIZE: 13.5pt; FONT-FAMILY: 宋体">球加</span><span 
            lang="EN-US" style="FONT-SIZE: 13.5pt; FONT-FAMILY: Arial, sans-serif">1</span><span 
            style="FONT-SIZE: 13.5pt; FONT-FAMILY: 宋体">蓝球的双色球号码进行投注。在普通玩法内，双色球胆拖并没有要求胆码一定要中奖才能</span><span 
            lang="EN-US" style="FONT-SIZE: 13.5pt; FONT-FAMILY: Arial, sans-serif"><br />
        </span><span style="FONT-SIZE: 13.5pt; FONT-FAMILY: 宋体">
        兑奖的规则，胆拖选出来的号码依然照普通玩法的兑奖方式兑奖。</span><span lang="EN-US" 
            style="FONT-SIZE: 13.5pt; FONT-FAMILY: Arial, sans-serif"><O:P></O:P></span></p>
    <p align="left" class="MsoNormal" style="MARGIN-LEFT: 36pt; TEXT-INDENT: -18pt">
        &lt;!--[if !supportLists]--&gt;<span lang="EN-US" 
            style="FONT-SIZE: 13.5pt; FONT-FAMILY: Arial, sans-serif">4.<span 
            style="FONT-SIZE: 7pt; FONT-FAMILY: 'Times New Roman'">&nbsp;&nbsp; </span>
        </span>&lt;!--[endif]--&gt;<span style="FONT-SIZE: 13.5pt; FONT-FAMILY: 宋体">过滤：</span><span 

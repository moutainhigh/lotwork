    <p>
        <b><span style="FONT-SIZE: 13.5pt; FONT-FAMILY: 宋体">一、怎么玩双色球？</span><span 
            lang="EN-US" style="FONT-SIZE: 13.5pt; FONT-FAMILY: Arial, sans-serif">&nbsp;</span></b></p>
    <p>
        &nbsp;</p>
    <p align="left" class="MsoNormal" style="MARGIN-LEFT: 30pt">
        <span style="FONT-SIZE: 13.5pt; FONT-FAMILY: 宋体">双色球投注区分为红球号码区与蓝球号码区。</span><span 
            lang="EN-US" style="FONT-SIZE: 13.5pt; FONT-FAMILY: Arial, sans-serif"><br />
        </span><span style="FONT-SIZE: 13.5pt; FONT-FAMILY: 宋体">其中，红球号码包括</span><span 
            lang="EN-US" style="FONT-SIZE: 13.5pt; FONT-FAMILY: Arial, sans-serif">01,02,…33</span><span 
            style="FONT-SIZE: 13.5pt; FONT-FAMILY: 宋体">，共</span><span lang="EN-US" 
            style="FONT-SIZE: 13.5pt; FONT-FAMILY: Arial, sans-serif">33</span><span 
            style="FONT-SIZE: 13.5pt; FONT-FAMILY: 宋体">个号码；蓝球号码包括</span><span 
            lang="EN-US" style="FONT-SIZE: 13.5pt; FONT-FAMILY: Arial, sans-serif">01, 
        02, …16</span><span style="FONT-SIZE: 13.5pt; FONT-FAMILY: 宋体">，共</span><span 
            lang="EN-US" style="FONT-SIZE: 13.5pt; FONT-FAMILY: Arial, sans-serif">16</span><span 
            style="FONT-SIZE: 13.5pt; FONT-FAMILY: 宋体">个号码。</span><span lang="EN-US" 
            style="FONT-SIZE: 13.5pt; FONT-FAMILY: Arial, sans-serif"><br />
        </span><span style="FONT-SIZE: 13.5pt; FONT-FAMILY: 宋体">投注的方式是至少选取</span><span 
            lang="EN-US" style="FONT-SIZE: 13.5pt; FONT-FAMILY: Arial, sans-serif">6</span><span 
            style="FONT-SIZE: 13.5pt; FONT-FAMILY: 宋体">个红球号码加上</span><span lang="EN-US" 
            style="FONT-SIZE: 13.5pt; FONT-FAMILY: Arial, sans-serif">1</span><span 
            style="FONT-SIZE: 13.5pt; FONT-FAMILY: 宋体">个蓝球号码组成一注。</span><span 
            lang="EN-US" style="FONT-SIZE: 13.5pt; FONT-FAMILY: Arial, sans-serif"><br />
        </span><span style="FONT-SIZE: 13.5pt; FONT-FAMILY: 宋体">简单的说，所选的</span><span 
            lang="EN-US" style="FONT-SIZE: 13.5pt; FONT-FAMILY: Arial, sans-serif">6</span><span 
            style="FONT-SIZE: 13.5pt; FONT-FAMILY: 宋体">红</span><span lang="EN-US" 
            style="FONT-SIZE: 13.5pt; FONT-FAMILY: Arial, sans-serif">+1</span><span 
            style="FONT-SIZE: 13.5pt; FONT-FAMILY: 宋体">蓝的双色球组合当中，只要猜中蓝球即可中奖。</span><span 
            lang="EN-US" style="FONT-SIZE: 13.5pt; FONT-FAMILY: Arial, sans-serif">&nbsp;</span></p>
    <p align="left" class="MsoNormal" style="MARGIN-LEFT: 30pt">
        <span lang="EN-US" style="FONT-SIZE: 13.5pt; FONT-FAMILY: Arial, sans-serif"><O:P></O:P>
        </span>
    </p>

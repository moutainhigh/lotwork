package com.winterframework.firefrog.user.dao.vo;

public class UserSlotExchangeCount {
	private Long exchangeAmount;
	private Long totleCount;
	private Long noBindingsCount;
	private Long noAwardCount;

	public Long getExchangeAmount() {
		return exchangeAmount;
	}

	public void setExchangeAmount(Long exchangeAmount) {
		this.exchangeAmount = exchangeAmount;
	}

	public Long getTotleCount() {
		return totleCount;
	}

	public void setTotleCount(Long totleCount) {
		this.totleCount = totleCount;
	}

	public Long getNoBindingsCount() {
		return noBindingsCount;
	}

	public void setNoBindingsCount(Long noBindingsCount) {
		this.noBindingsCount = noBindingsCount;
	}

	public Long getNoAwardCount() {
		return noAwardCount;
	}

	public void setNoAwardCount(Long noAwardCount) {
		this.noAwardCount = noAwardCount;
	}

}

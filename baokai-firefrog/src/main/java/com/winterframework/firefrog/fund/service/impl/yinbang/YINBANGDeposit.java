package com.winterframework.firefrog.fund.service.impl.yinbang;


public class YINBANGDeposit {
	private String encParam;
	private String merId;
	private String sign;
	private String version;
	private String transDomain;
	private String chargeUrl;
	public String getEncParam() {
		return encParam;
	}
	public void setEncParam(String encParam) {
		this.encParam = encParam;
	}
	public String getMerId() {
		return merId;
	}
	public void setMerId(String merId) {
		this.merId = merId;
	}
	public String getSign() {
		return sign;
	}
	public void setSign(String sign) {
		this.sign = sign;
	}
	public String getVersion() {
		return version;
	}
	public void setVersion(String version) {
		this.version = version;
	}
	public String getChargeUrl() {
		return chargeUrl;
	}
	public void setChargeUrl(String chargeUrl) {
		this.chargeUrl = chargeUrl;
	}
	public String getTransDomain() {
		return transDomain;
	}
	public void setTransDomain(String transDomain) {
		this.transDomain = transDomain;
	}
	
	
	

}

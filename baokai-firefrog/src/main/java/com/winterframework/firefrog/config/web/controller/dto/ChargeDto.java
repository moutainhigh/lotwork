package com.winterframework.firefrog.config.web.controller.dto;

public class ChargeDto {

	private Long charge_ratio;
	private Long type;
	private Long bel;

	public Long getCharge_ratio() {
		return charge_ratio;
	}

	public void setCharge_ratio(Long charge_ratio) {
		this.charge_ratio = charge_ratio;
	}

	public Long getType() {
		return type;
	}

	public void setType(Long type) {
		this.type = type;
	}

	public Long getBel() {
		return bel;
	}

	public void setBel(Long bel) {
		this.bel = bel;
	}

}

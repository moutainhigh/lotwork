CREATE OR REPLACE FORCE VIEW FIREFOG.VIEW_GENERAL_ADNOTICE
(
   ID,
   TYPE,
   TITLE,
   CONTENT,
   GMT_EFFECT_BEGIN,
   GMT_EFFECT_END,
   OPERATOR,
   GMT_CREATED,
   SHOW_PAGES,
   STATUS,
   RC_ALL,
   RC_TOP_AGENT,
   RC_OT_AGENT,
   RC_VIP,
   RC_NON_VIP,
   RC_CUSTOMER,
   MEMO,
   APPROVER,
   GMT_APPR,
   GMT_MODIFIED,
   NOTICELEVEL,
   USERID
)
AS
     SELECT adn."ID",
            adn."TYPE",
            adn."TITLE",
            adn."CONTENT",
            adn."GMT_EFFECT_BEGIN",
            NVL (adn."GMT_EFFECT_END", SYSDATE + 1) AS GMT_EFFECT_END,
            adn."OPERATOR",
            adn."GMT_CREATED",
            NVL (adn."SHOW_PAGES", 'all') AS show_pages,
            adn."STATUS",
            adn."RC_ALL",
            adn."RC_TOP_AGENT",
            adn."RC_OT_AGENT",
            adn."RC_VIP",
            adn."RC_NON_VIP",
            adn."RC_CUSTOMER",
            adn."MEMO",
            adn."APPROVER",
            adn."GMT_APPR",
            adn."GMT_MODIFIED",
            adn."NOTICELEVEL",
            USERLEVEL.USERID
       FROM AD_NOTICE adn,
            (SELECT DECODE (UC.VIP_LVL, 0, '1', '-1') AS nonVip,
                    DECODE (UC.VIP_LVL, 0, '-1', '1') AS vip,
                    DECODE (UC.USER_LVL, -1, '1', '-1') AS cus,
                    DECODE (UC.USER_LVL, 0, '1', '-1') AS top,
                    CASE WHEN UC.USER_LVL > 0 THEN '1' ELSE '-1' END AS othe,
                    UC.ID AS userId
               FROM USER_CUSTOMER uc) userLevel
      WHERE     GMT_EFFECT_BEGIN <= SYSDATE
            AND STATUS = 3
            AND (   ADN.RC_ALL = 1
                 OR (   ADN.RC_CUSTOMER = USERLEVEL.CUS
                     OR ADN.RC_NON_VIP = USERLEVEL.NONVIP
                     OR ADN.RC_OT_AGENT = USERLEVEL.OTHE
                     OR ADN.RC_TOP_AGENT = USERLEVEL.TOP
                     OR ADN.RC_VIP = USERLEVEL.VIP))
   ORDER BY GMT_EFFECT_BEGIN DESC;
   
   
   
   
   

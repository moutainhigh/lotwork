package com.winterframework.firefrog.fund.util;

public class SummaryAndTrade {

	private Long number;
	private String frontSummary;
	private String backSummart;
	private String tradeType;
	private String tradeCode;
	public Long getNumber() {
		return number;
	}
	public void setNumber(Long number) {
		this.number = number;
	}
	public String getFrontSummary() {
		return frontSummary;
	}
	public void setFrontSummary(String frontSummary) {
		this.frontSummary = frontSummary;
	}
	public String getBackSummart() {
		return backSummart;
	}
	public void setBackSummart(String backSummart) {
		this.backSummart = backSummart;
	}
	public String getTradeType() {
		return tradeType;
	}
	public void setTradeType(String tradeType) {
		this.tradeType = tradeType;
	}
	public String getTradeCode() {
		return tradeCode;
	}
	public void setTradeCode(String tradeCode) {
		this.tradeCode = tradeCode;
	}
	

}

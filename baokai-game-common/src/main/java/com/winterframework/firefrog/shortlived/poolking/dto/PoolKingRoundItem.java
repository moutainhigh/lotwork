package com.winterframework.firefrog.shortlived.poolking.dto;

public class PoolKingRoundItem {
	
	private String name;
	
	private Integer spendMoney;
	
	private Integer count;
	
	private Long bouns;

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public Integer getSpendMoney() {
		return spendMoney;
	}

	public void setSpendMoney(Integer spendMoney) {
		this.spendMoney = spendMoney;
	}

	public Integer getCount() {
		return count;
	}

	public void setCount(Integer count) {
		this.count = count;
	}

	public Long getBouns() {
		return bouns;
	}

	public void setBouns(Long bouns) {
		this.bouns = bouns;
	}

}

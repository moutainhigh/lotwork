package com.winterframework.firefrog.phone.web.cotroller.dto;

public class LoginGameGroup {

	private String awardName;
	private String awardGroupId;
	private String lotteryId;
	private String lotterySeriesCode;
	public String getAwardName() {
		return awardName;
	}

	public void setAwardName(String awardName) {
		this.awardName = awardName;
	}

	public String getAwardGroupId() {
		return awardGroupId;
	}

	public void setAwardGroupId(String awardGroupId) {
		this.awardGroupId = awardGroupId;
	}

	public String getLotteryId() {
		return lotteryId;
	}

	public void setLotteryId(String lotteryId) {
		this.lotteryId = lotteryId;
	}

	public String getLotterySeriesCode() {
		return lotterySeriesCode;
	}

	public void setLotterySeriesCode(String lotterySeriesCode) {
		this.lotterySeriesCode = lotterySeriesCode;
	}

}
